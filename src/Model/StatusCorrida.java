package Model;

import java.util.ArrayList;
import java.util.List;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author Tadeu
 */
@Entity
@Table(name = "statusCorrida")
@XmlRootElement
public class StatusCorrida {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "CODSTATUSCORRIDA")
    private Integer codStatusCorrida;
    @Basic(optional = false)
    @Column(name = "DESCRICAO",unique = true)
    private String descricao;

    public Integer getCodStatusCorrida() {
        return codStatusCorrida;
    }

    public void setCodStatusCorrida(Integer codStatusCorrida) {
        this.codStatusCorrida = codStatusCorrida;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

}
