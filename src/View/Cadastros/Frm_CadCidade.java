    private void salvar() {
        try {
            cidade = new Cidade();
            cidadesDAO = new CidadesDAO();
            cidade.setDescricao(txt_cidade.getText());
            cidadesDAO.salvar(cidade);
            JOptionPane.showMessageDialog(null, "Cidade salva com sucesso!");
        } catch (Exception e) {
            if (e.toString().contains("com.mysql.jdbc.exceptions.jdbc4.MySQLIntegrityConstraintViolationException")) {
                JOptionPane.showMessageDialog(null, "Cidade já cadastrada!");
                txt_cidade.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao salvar a cidade!\n" + e);
            }
        }finally{
            txt_cidade.setText(null);
            txt_cidade.requestFocus();
        }
    }
